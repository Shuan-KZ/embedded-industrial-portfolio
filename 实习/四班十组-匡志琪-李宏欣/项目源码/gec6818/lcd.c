/*
    功能: 打开显示屏
*/
void open_lcd()
{
    fd_lcd = open("/dev/fb0", O_RDWR);
    if(fd_lcd == -1)
    {
        perror("open /dev/fb0 error");
        exit(1);
    }
    printf("open /dev/fb0 success\n");
}

/*
    功能: 关闭显示屏
*/
void close_lcd()
{
    close(fd_lcd);
}


/*
	功能： LCD初始化: 打开显示屏，内存映射
*/
void lcd_init()
{
	//打开显示屏
	open_lcd();

	//内存映射
	plcd = mmap(
		NULL,	   //NULL表示让操作系统自行分配映射区域的位置
		800*480*4, //映射区域的大小
		PROT_READ|PROT_WRITE, //可读可写
		MAP_SHARED,//共享映射
		fd_lcd,	   //需要进行映射的文件对应的文件描述符
		0		   //0表示从文件开头进行映射
	);
	if(plcd == MAP_FAILED)
	{
		perror("mmap error");
		exit(1);
	}
	printf("mmap success\n");
}

/*
	功能: 
		解除显示屏初始化: 解除内存映射，关闭显示屏
*/
void lcd_uninit()
{
	//解除内存映射
	munmap(plcd, 800*480*4);
	//关闭显示屏
	close_lcd();
}

/*
	功能: 把一个指定位置的像素点显示某种颜色
	参数:
		(x, y)： 第y行的第x个像素点
		color: 颜色数据
*/
void lcd_draw_point(int x, int y, unsigned int color)
{
    if(x >= 0 && x < 800 && y >= 0 && y < 480)
    {
        *(plcd + 800*y + x) = color;
    }
}

/*
	功能: 画背景色
	参数:	
		color: 背景的颜色数据
*/
void lcd_draw_background(unsigned int color)
{
	for(int i = 0; i < 480; i++) //行
	{
		for(int j = 0; j < 800; j++) //列
		{
			lcd_draw_point(j, i, color);
		}
	}
}


/*
	功能: 获取BMP图片的关键信息
	参数:
		bmppath: 图片路径
	返回值:
		返回图片的信息
*/
BMP get_bmp_info(char* bmppath)
{

	//打开BMP图片
	int fd_bmp = open(bmppath, O_RDONLY);
	if(fd_bmp == -1)
	{
		perror("open bmp error");
		printf("open %s error\n", bmppath);
		exit(1);
	}

	//获取魔数，判断该文件是否为BMP文件
	char BM[2] = {0};
	read(fd_bmp, BM, 2);

	if(!(BM[0] == 'B' && BM[1] == 'M'))
	{
		printf("%s is not the bmpfile\n", bmppath);
		exit(1);
	}

	BMP bmp;

	//图片的大小
	lseek(fd_bmp, 0x02, SEEK_SET); //从文件开头偏移2个字节的位置
	read(fd_bmp, &bmp.size, 4);

	//图片的宽度和高度
	lseek(fd_bmp, 0x12, SEEK_SET);
	read(fd_bmp, &bmp.width, 4);
	read(fd_bmp, &bmp.height, 4);

	//图片的色深
	lseek(fd_bmp, 0x1C, SEEK_SET);
	read(fd_bmp, &bmp.depth, 2);

	//像素数组
	char* p = malloc(bmp.size - 54); //申请一块空间用来存储像素数组的数据
	lseek(fd_bmp, 0x36, SEEK_SET); //前面54个字节是文件属性信息
	read(fd_bmp, p, bmp.size-54);
	bmp.data = p;

	close(fd_bmp);

	return bmp;
}


/*
	功能: 显示一张BMP图片
	参数:
		(x, y): 把第y行的第x个像素点作为图片的左上角坐标
		bmppath: BMP图片的路径
*/
void lcd_draw_bmp(int x, int y, char* bmppath)
{
	//获取图片属性信息
	BMP bmp = get_bmp_info(bmppath);
	
	printf("bmppath = %s, size = %d, width = %d, height = %d, depth = %d\n",
	        bmppath, bmp.size, bmp.width, bmp.height, bmp.depth);

	//我们这里只考虑24位和宽度为4的倍数的图片
	//解析像素数组中的数据，并将其画到开发板上
	unsigned int color; //颜色数据
	unsigned char r, g, b;
	char* p = bmp.data;

	for(int h = 0; h < bmp.height; h++)
	{
		for(int w = 0; w < bmp.width; w++)
		{
			//获取颜色分量
			b = *(p++);
			g = *(p++);
			r = *(p++);
			//将颜色分量合成为颜色数据
			color = r<<16 | g<<8 | b;

			//width > 0: 每一行的像素点从左往右存放
			//width < 0: 每一行的像素点从右往左存放
			//height > 0: 每一列的像素点从下往上存放
			//height < 0: 每一列的像素点从上往下存放
			lcd_draw_point(x + w, y+bmp.height-1 -h, color);
		}
	}

	free(bmp.data); //释放申请的空间
}



/*
	功能: 显示一个字符
	参数:
		(x0, y0): 设置第y0行的第x0个像素点作为字符左上角的坐标
		w: 字符的宽
		h: 字符的高
		word: 字符的取模数据
		color: 字符的颜色
*/
void lcd_draw_word(int x0, int y0, int w, int h, unsigned char word[], unsigned int color)
{
	int x, y; //表示需要显示的像素点坐标
	//对字模数据进行遍历
	for(int i = 0; i < w*h/8; i++)
	{
		//一个一个bit进行解析，从左到右
		for(int j = 0; j < 8; j++)
		{
			if(word[i] & (1<<(7-j)))
			{
				y = i/(w/8); //第几行 w/8表示每行有几个元素
				x = i%(w/8)*8 + j;
				//i%(w/8) 当前元素这一行前面有几个元素
				//i%(w/8)*8 当前元素这一行前面有几个像素点
				lcd_draw_point(x+x0, y+y0, color);
			}
		}
	}
}

/*
	功能: 显示数字
	参数:
		num: 需要显示的数字
		(x0, y0): 作为最右侧的那个数字的坐标
		w: 字符的宽度
		h: 字符的高度
		color: 显示的数字的颜色
		
*/
void lcd_draw_num(int num, int x0, int y0, int w, int h, unsigned int color)
{
	int i = 0;
	if (num < 0) num = -num;
	do {
		lcd_draw_word(x0-i*(w+5), y0, w, h, nums[num%10], color);
		num = num/10;
		i++;
	} while (num != 0);
}





















